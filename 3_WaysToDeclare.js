/*
let:
1. The let keyword was introduced in ES6.

2. Variables defined with let cannot be Redeclared but can be updated

3. Variables defined with let must be Declared before use.

4. Variables defined with let have Block Scope.

*/
let x = "Swagata Katiyar";

/*
Const: 

1. The const keyword was introduced in ES6 (2015).

2. Variables defined with const cannot be Redeclared.

3. Variables defined with const cannot be Reassigned.

4. Variables defined with const have Block Scope.

5. Const must be assigned at time of declaration of variable

*/

const PI = 3.141592653589793;


/*
var:  

The var statement declares a function-scoped or globally-scoped variable, optionally initializing it to a value.

*/

var name= "Swagata"
